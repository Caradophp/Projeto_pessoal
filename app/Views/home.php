    <h2>História</h2>
    <p>
        A FinanTech foi fundada em 1º de março de 2010,
       por um grupo de especialistas em finanças e tecnologia, 
       com o objetivo de preencher a lacuna no mercado de soluções 
       financeiras inteligentes e acessíveis. Inicialmente, a empresa 
       começou com apenas três colaboradores e um foco em consultoria 
       financeira para pequenos empresários. Com o passar dos anos, a 
       FinanTech foi se expandindo, e em 2014, introduziu sua primeira 
       plataforma digital, revolucionando o mercado com a possibilidade 
       de gerenciar investimentos, orçamentos e contas bancárias de forma
        automatizada.

      Em 2017, 
      a empresa 
      passou a oferecer 
      serviços de crédito 
      digital, tornando-se uma das pioneiras no uso
       de inteligência artificial para avaliação de crédito. Hoje, 
       a FinanTech é um dos maiores nomes do setor financeiro digital no Brasil, 
       com milhares de clientes que confiam em nossas soluções para gerenciar seu 
       dinheiro e realizar seus sonhos. Continuamos inovando e trabalhando para 
       garantir que todos tenham acesso a um futuro financeiro mais próspero e seguro.
